class Signup < ActiveRecord::Base  
  
  attr_accessible :competition_id, :user_id
  
  # ===================================================================================================================
  # ASSOCIATIONS
  # ===================================================================================================================
  belongs_to :competition
  belongs_to :user


  # ===================================================================================================================
  # VALIDATION
  # ===================================================================================================================
  validates :user_id, :uniqueness => { :scope => :competition_id } # user can sign up to competition only once

  
end
